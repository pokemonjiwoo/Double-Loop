#숫자 4개의 덧셈 함수, 곱셈 함수 코드를 작성하시오

def add_numbers(num1, num2, num3, num4):
    result = num1+num2+num3+num4
    return result
a = add_numbers(1, 2, 3, 4)
print(a)

def mul_numbers(num1, num2, num3, num4):
    result = num1*num2*num3*num4
    return result
b = mul_numbers(num1=1, num2=2, num3=4, num4=4)
print(b)
    